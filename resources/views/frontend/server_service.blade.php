@section('footer_script')
<script>
    $(document).ready(function() {
        var allServices = [];
        function renderServiceList(services) {
            $('#service-list').empty();
            if (services.length > 0) {
                services.forEach(function(service) {
                    var formattedPrice = currencyICON + parseFloat(service.price).toFixed(2);
                    var serviceList = `
                        <a class="col-md-6 col-lg-4 mb-4" href="{{ route('server_service_view', '') }}/${service.slug}">
                            <div class="card h-100">
                                <div class="card-body d-flex py-2">
                                    <div class="d-flex gap-3 align-items-center">
                                        <div class="flex-shrink-0" style="width: 100px;">
                                            <img src="${service.thumbnail}" alt="${service.title}" class="rounded" width="100" height="67">
                                        </div>
                                        <div class="flex-grow-1">
                                            <p style="font-size:13px; margin-bottom:8px" class="text-wrap">${service.title}</p>
                                            <div>
                                                <span class="badge bg-light-success" style="font-size:12px">${formattedPrice}</span>
                                                <span class="badge bg-light-warning" style="font-size:12px">${service.delivery_time}</span>
                                            </div>
                                        </div>
                                    </div>
                                </div>
                            </div>
                        </a>
                    `;
                    $('#service-list').append(serviceList);
                });
                $('#no-results-message').hide();
            } else {
                $('#no-results-message').show();
            }
        }
        $.ajax({
            url: "{{ route('fetch_server_data') }}",
            type: 'GET',
            beforeSend: function() {
                $('.top-loader').show();
                $('.page-top-info-loader').show();
                $('.page-top-info').hide();
            },
            complete: function() {
                $('.top-loader').hide();
                $('.page-top-info-loader').hide();
                $('.page-top-info').show();
            },
            success: function(response) {
                allServices = response.service_list;
                currencyICON = response.currencyIcon;
                renderServiceList(allServices);
            },
        });
        $('#search-input').on('keyup', function() {
            var searchQuery = $(this).val().toLowerCase();
            var filteredServices = allServices.filter(function(service) {
                return service.title.toLowerCase().includes(searchQuery);
            });
            renderServiceList(filteredServices);
        });
    });
</script>
